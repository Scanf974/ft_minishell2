#include <ft_minishell.h>

void	ft_key_ctrl_v(char *buf, int *pos)
{
	int		i;

	i = 0;
	while (i < 8 && buf[i])
	{
		ft_key_printable(&buf[i], pos);
		i++;
	}
}
